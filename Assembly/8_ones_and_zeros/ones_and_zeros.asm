

		AREA PROGRM,CODE,READONLY
		MOV R2,#0
		MOV R3,#0
		MOV R7,#2
		MOV R0,#0X40000000
LOOP	LDR R1, [R0]
		MOV R4, #32
REPEAT 	RORS R1 ,#1
		BCS ONES
		ADD R3,#1
		B NEXT
		
