


		AREA LOOKUP, CODE, READONLY
		ENTRY
		MOV R5,#4
NXTRND	MOV R4,R5
		LDR R0, MEM
NXTCOMP	LDRB R1,[R0]
		ADD R0,#1
		LDRB R2,[R0]
		CMP R1,R2
		BHS NOSWAP
		STRB R1, [R0], #-1
		STRB R2, [R0], #1
NOSWAP 	SUBS R4, #1
		BNE NXTCOMP
		SUBS R5, #1
		BNE NXTRND
STOP	B	STOP
MEM		DCD 0X40000000
		END
